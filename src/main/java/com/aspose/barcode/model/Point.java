package com.aspose.barcode.model;

/**
 *
 * @author Imran Anwar
 */
public class Point {
	private float X = 0.0f;
	private float Y = 0.0f;
	
  /**
	 * getX
	 * Gets Float
	 * @return X
	 */
  public float getX() {
    return X;
  }

	/**
	 * setX
	 * Sets Float
	 * @param X float
	 */
  public void setX(float X) {
    this.X = X;
  }
  /**
	 * getY
	 * Gets Float
	 * @return Y
	 */
  public float getY() {
    return Y;
  }

	/**
	 * setY
	 * Sets Float
	 * @param Y float
	 */
  public void setY(float Y) {
    this.Y = Y;
  }  
}
